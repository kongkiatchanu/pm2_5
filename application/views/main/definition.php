			<h4>ดัชนีคุณภาพอากาศ</h4>
			<p><strong>ดัชนีคุณภาพอากาศ</strong> (Air Quality Index : AQI) เป็นการรายงานข้อมูลคุณภาพอากาศในรูปแบบที่ง่ายต่อความเข้าใจของประชาชนทั่วไป เพื่อเผยแพร่ประชาสัมพันธ์ให้ประชาชนได้ทราบถึงสถานการณ์มลพิษทางอากาศในแต่ละพื้นที่ รวมถึงผลกระทบจากมลพิษทางอากาศต่อสุขภาพ ซึ่งดัชนีคุณภาพอากาศ 1 ค่า ใช้เป็นตัวแทนค่าความเข้มข้นของสารมลพิษทางอากาศ 6 ชนิด ได้แก่</p>
			<ul>
				<li>ฝุ่นละอองขนาดไม่เกิน 2.5 ไมครอน (PM<sub>2.5</sub>) เป็นฝุ่นที่มีเส้นผ่านศูนย์กลางไม่เกิน 2.5 ไมครอน เกิดจากการเผาไหม้เชื้อเพลิงของยานพาหนะ การเผาวัสดุการเกษตร ไฟป่า และกระบวนการอุตสาหกรรม และเนื่องจากฝุ่นมีขนาดเล็กจึงง่ายต่อการแพร่กระจายสู่ถุงลมปอด ส่งผลให้เกิดโรคระบบทางเดินหายใจ และโรคปอดต่าง ๆ ซึ่งหากได้รับในปริมาณมากหรือเป็นระยะเวลานาน จะทำให้เกิดการสะสมในเนื้อเยื่อปอด ประสิทธิภาพการทำงานของปอดลดลง เกิดหลอดลมอักเสบและมีอาการหอบหืด</li>
				<li>ฝุ่นละอองขนาดไม่เกิน 10 ไมครอน (PM<sub>10</sub>) เป็นฝุ่นที่มีขนาดเส้นผ่านศูนย์กลางไม่เกิน 10 ไมครอน เกิดจากการเผาไหม้เชื้อเพลิง การเผาในที่โล่ง กระบวนการอุตสาหกรรม การบด การโม่ หรือฝุ่นผงจากการก่อสร้าง หากหายใจนำฝุ่นเข้าสู่ร่างกายจะเกิดการสะสมในระบบทางเดินหายใจ ส่งผลเสียต่อสุขภาพ </li>
				<li>ก๊าซโอโซน (O<sub>3</sub>) เป็นสารมลพิษทางอากาศในชั้นบรรยากาศผิวโลก เกิดจากปฏิกิริยาระหว่างก๊าซออกไซด์ของไนโตรเจน และสารประกอบอินทรีย์ระเหยง่าย โดยมีแสงแดดเป็นตัวเร่งปฏิกิริยา มีผลกระทบต่อสุขภาพ โดยก่อให้เกิดการระคายเคืองตาและระคายเคืองต่อระบบทางเดินหายใจและเยื่อบุต่างๆ ทำให้การทำงานของปอดลดลง เหนื่อยเร็ว โดยเฉพาะในเด็ก คนชรา และคนที่เป็นโรคปอดเรื้อรัง</li>
				<li>ก๊าซคาร์บอนมอนอกไซด์ (CO) เป็นก๊าซที่เกิดจากการเผาไหม้ไม่สมบูรณ์ของเชื้อเพลิงที่มีคาร์บอนเป็นองค์ประกอบ สามารถสะสมอยู่ในร่างกายโดยการรวมตัวกับฮีโมโกลบินในเม็ดเลือดแดง ซึ่งหากหายใจนำก๊าซชนิดนี้ในร่างกาย ก๊าซคาร์บอนมอนอกไซด์จะทำปฏิกิริยากับฮีโมโกลบินในเลือด เกิดเป็นคาร์บอกซีฮีโมโกลบิน (CoHb) ทำให้การลำเลียงออกซิเจนไปสู่เซลล์ต่างๆ ของร่างกายลดน้อยลง ส่งผลให้ร่างกายเกิดอาการอ่อนเพลีย และหัวใจทำงานหนักขึ้น</li>
				<li>ก๊าซไนโตรเจนไดออกไซด์ (NO<sub>2</sub>) เป็นก๊าซที่มีอยู่ทั่วไปในธรรมชาติ หรือเกิดจากการกระทำของมนุษย์ เช่น การเผาไหม้เชื้อเพลิงต่างๆ อุตสาหกรรมบางชนิด เป็นต้น ก๊าซนี้มีผลต่อระบบการมองเห็นและผู้ที่มีอาการหอบหืดหรือ โรคเกี่ยวกับทางเดินหายใจ</li>
				<li>ก๊าซซัลเฟอร์ไดออกไซด์ (SO<sub>2</sub>) เป็นก๊าซที่เกิดจากธรรมชาติและการเผาไหม้เชื้อเพลิงที่มีกำมะถัน (ซัลเฟอร์) เป็นส่วนประกอบ สามารถรวมตัวกับสารมลพิษอื่นแล้วก่อตัวเป็นอนุภาคฝุ่นขนาดเล็ก ก๊าซนี้มีผลกระทบโดยตรงต่อสุขภาพ ทำให้เกิดการระคายเคืองต่อเยื่อบุตา ผิวหนัง และระบบทางเดินหายใจ หากได้รับเป็นเวลานาน ๆ จะทำให้เป็นโรคหลอดลมอักเสบเรื้อรังได้้</li>
			</ul>
			
